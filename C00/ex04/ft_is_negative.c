#include <unistd.h>

void	ft_is_negative(int n)
{
	char	b;
	char	a;

	b = 'N';
	a = 'P';
	if (n < 0)
	{
		write(1, &b, 1);
	}
	else
	{
		write(1, &a, 1);
	}
}
